# 정답 데이터: (회차, 레벨, 선택지수, [정답,배점] x 50)
# answer: 1~5 (0 = 없음/전원정답)

exams = []

def add_exam(session, level, num_choices, data):
    """data: list of (answer, points) tuples, 50 items"""
    answers = []
    for i, (ans, pts) in enumerate(data):
        answers.append({
            "questionNo": i + 1,
            "answer": ans,
            "points": pts
        })
    exams.append({
        "session": session,
        "level": level,
        "levelKr": "심화" if level == "advanced" else "기본",
        "numChoices": num_choices,
        "totalQuestions": 50,
        "totalPoints": 100,
        "answers": answers
    })

